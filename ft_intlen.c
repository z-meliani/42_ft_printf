#include "ft_printf.h"

int	ft_intlen(int n)
{
	int	len;

	len = 0;
	if (!n)
		return (0);
	if (n < 0)
		len++;
	while (n / 10 != 0)
	{
		n /= 10;
		len++;
	}
	len++;
	return (len);
}
